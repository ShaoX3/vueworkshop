// ==|== Imports ===================================================================================
import { createStore } from 'vuex';

// ==|== Store =====================================================================================
export default createStore({
  // ==|== State ===================================================================================
  state: {
    count: 0
  },

  // ==|== Actions =================================================================================
  actions: {
    setCount({ commit }) {
      commit('setCount');
    }
  },

  // ==|== Mutations ===============================================================================
  mutations: {
    setCount(state) {
      state.count = state.count + 1;
    }
  },

  // ==|== Getters =================================================================================
  getters: {
    count: (state) => {
      return state.count;
    }
  }
});
